package Week1;

public class Exercise4VaryingVariables {
    public static void main(String[] args) {
        int Chickens = 3; // aici numesti variabila chickens.
        System.out.println("Chickens: ");
        System.out.println(Chickens);
        double Bacon = 5.5; //am kg de bacon 5.5 adica double(cu virgula)
        System.out.println("Bacon(kg):");
        System.out.println(Bacon);
        String Tractor = "Tractor:";
        System.out.println(Tractor);

        int Chickens2 = 9000; // aici numesti variabila chickens.
        System.out.println("Chickens: ");
        System.out.println(Chickens2);
        double Bacon2 = 0.1; //am kg de bacon 5.5 adica double(cu virgula)
        System.out.println("Bacon(kg):");
        System.out.println(Bacon2);
        String Tractor2 = "Zetor";
        System.out.println(Tractor2);
    }
}
